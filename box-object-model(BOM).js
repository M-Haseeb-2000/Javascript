function resizeFunction(){
    console.clear();
    var inner_Height = window.innerHeight;
    console.log('inner height: ' + inner_Height);
    var outer_Height = window.outerHeight;
    console.log('outer height' + outer_Height);
    var inner_width = window.innerWidth;
    console.log('inner width: ' + inner_width);
    var outer_Width = window.outerWidth;
    console.log('outer width' + outer_Width);
}